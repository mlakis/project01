	     <h2>North American Foods</h2>
	     <h3>History</h3>
	     <p>
	     	North American cuisine is a term used for foods native to or popular in countries of North America, such as Canadian cuisine, American cuisine, Mexican cuisine and Central American cuisine. North American cuisines display influence from many international cuisines, including Native American cuisine, Jewish cuisine, Asian cuisine, and especially European cuisine. As a broad, geo-culinary term, North American cuisine also includes Central American and Caribbean cuisines. These regions are part of North America, so these regional cuisines also fall within the penumbra of North American cookery.
	     </p>
	     <h3>American Foods</h3>
		<figure>
			<img src="images/american.jpg" alt="American food">
		</figure>
	     <p>
	     	The cuisine of the United States combines the cuisines of the Native Americans, Asians, and Europeans. When immigrants came to the New World, they introduced new dishes and recipes. The Spanish and Aztecs made Mexican cuisine what it is today. The indigenous people had a diet of corn, tomatoes, peppers, and various herbs. Also, it was the New World that came up with chocolate. The Aztecs drank chocolate during religious rituals. Explorers tried chocolate in Mexico and were so delighted by it that decided to bring it back to their home countries. The conquistadors, on the other hand, contributed animals (chicken, pigs, sheep, and goats), barley, rice, olive oil, wheat, parsley, nuts, and a variety of spices. Central American food was influenced by many cuisines. African, European, Native American, and Asian cuisines all had a role in making Central American cuisine. Africans gave cooking techniques, Europeans brought new fruits and vegetables, Native Americans gave corn and other crops, and Asians introduced food styles and spices.
	     </p>
	     <p>
	     	Some dishes that are typically considered American have their origins in other countries. American cooks and chefs have substantially altered these dishes over the years, to the degree that the dishes now enjoyed around the world are considered to be American. Hot dogs and hamburgers are both based on traditional German dishes, but in their modern popular form they can be reasonably considered American dishes.[29]
	     </p>
	     <h3>Mexican Foods</h3>
		<figure>
			<img src="images/mexican.jpg" alt="Mexican food">
		</figure>
		<p>
			Mexican cuisine is primarily a fusion of indigenous Mesoamerican cooking with European, especially Spanish, elements added after the Spanish conquest of the Aztec Empire in the 16th century. The staples are native foods, such as corn, beans, avocados, tomatoes, and chili peppers, along with rice, which was brought by the Spanish. Europeans introduced a large number of other foods, the most important of which were meats from domesticated animals (beef, pork, chicken, goat, and sheep), dairy products (especially cheese), and various herbs and spices.
		</p>
		<p>
			Over the centuries, this resulted in regional cuisines based on local conditions, such as those in Oaxaca, Veracruz and the Yucatán Peninsula. Mexican cuisine is an important aspect of the culture, social structure and popular traditions of Mexico. The most important example of this connection is the use of mole for special occasions and holidays, particularly in the South and Center regions of the country. For this reason and others, traditional Mexican cuisine was inscribed in 2010 on the Representative List of the Intangible Cultural Heritage of Humanity by UNESCO.[2]
		</p>
		<p>
			The best known of Mexico’s street foods is the taco, whose origin is based on the pre-Hispanic custom of picking up other foods with tortillas as utensils were not used.[10] The origin of the word is in dispute, with some saying it is derived from Nahuatl and others from various Spanish phrases.[33] Tacos are not eaten as the main meal; they are generally eaten before midday or late in the evening. Just about any other foodstuff can be wrapped in a tortilla, and in Mexico, it varies from rice, to meat (plain or in sauce), to cream, to vegetables, to cheese, or simply with plain chile peppers or fresh salsa. Preferred fillings vary from region to region with pork generally found more often in the center and south, beef in the north, seafood along the coasts, and chicken and lamb in most of the country.[34]
		</p>

	
